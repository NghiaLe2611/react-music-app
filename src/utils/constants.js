function mapChordToFretPositions(chordFretPositions, tuning) {
	const notes = [];
	for (let i = 0; i < chordFretPositions.length; i++) {
		const fretPosition = chordFretPositions[i];
		const stringIndex = i;
		const note = guitarFrets[stringIndex][fretPosition];
		notes.push(note);
	}
	return notes;
}

